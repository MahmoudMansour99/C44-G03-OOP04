using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Assignment
{
    internal class Calculator
    {
        #region Methods
        public int Add(int A, int B)
        {
            return A + B;
        }
        public int Add(int A, int B, int C)
        {
            return A + B + C;
        }
        public double Add(double A, double B)
        {
            return A + B;
        }
        #endregion
    }
}
